#Terminology
    #1-database      - databases verilerin sistematik bir formatta saklandığı tablolardan oluşan bir koleksiyondur.
    #2-table         - bu tablolar yine satır ve sutunlardan oluşan bir veri matrisidir.
    #3-row           - tablolardaki veri sıraları
    #4-column        - tablolardaki  aynı türde verileri içeren gruplarını oluşturan kolonlar.yaş kolunu, cinsiyet kolunu gibi.
    #5-primary_key   - her bir veri sırasının benzersiz anahtarı, tc_kimlik_no gibi,tabloda iki kez kullanılamaz.
    #6-foreign_key   - İki tabloyu ve iki veri sırasının birbirine bağlarken kullanılan benzersiz anahtar.İki tablo arasındaki bağlantı pimidir.
    #7-compound_key  - Aslında birden fazla sütundan oluşan bir anatardır, dolayısıyla bazen bir sütun yeterli olmaz, bu nedenle kullanılır.
    #8-index         - index  bir nevi veritabanının kapağındaki içindekiler sayfasıdır.
    #9-redundancy